package logic;

public class CalculatorLogic {
    public long add(int temp, int temp2) {
        return temp+temp2;
    }

    public long subtract(int temp, int temp2) {
        return temp-temp2;
    }

    public double divide(int temp, int temp2) {
        return (double)temp/temp2;
    }

    public double multiple(int temp, int temp2) {
        return (double) temp *temp2;
    }
}
